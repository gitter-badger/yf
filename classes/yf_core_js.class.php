<?php

class yf_core_js {

	private $content_added = array();
	private $content_md5 = array();

	function _init() {
	}
	
	function add($content, $is_file = true, $params = array()) {
		// input: string/array		
		// todo: auto detection of script/link
		if (!is_array($content)) {
			$content = array(0 => $content);
		}
		foreach ($content as $v) {
			$v = trim($v);
			if (!strlen($v)) {
				continue;
			}
			if ($is_file) {
				$v = '<script src="'.htmlspecialchars($v, ENT_QUOTES).'" type="text/javascript"></script>';
			}
			$md5 = md5($v);
			if (empty($this->content_md5[$md5])) {
				$this->content_md5[$md5] = $md5;
				$this->content_added[] = $v;
			}
		}
	}
	
	function show_js($params = array()) {
		$content = tpl()->parse('script_js');
		if (strpos($content,"\n") === false) {
			// single string = automatically generated by compass
			$content = '<script src="'.htmlspecialchars(WEB_PATH. tpl()->TPL_PATH. $content).'" type="text/javascript"></script>';
		} 
		foreach ($this->content_added as $v) {
			$content .= $v;
		}
		return $content;
	}
}